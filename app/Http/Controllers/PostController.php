    public function store(Request $request)
    {
        $user = Auth::user();

        $inputs = \request()->validate([
            'title' => 'required',
            'description' => 'required',
            'image' => 'file'
        ]);

        if(request('image')){
            $inputs['image'] = \request('image')->store('images','public');
        }

        $user->posts()->create($inputs);
